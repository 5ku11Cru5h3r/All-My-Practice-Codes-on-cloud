class Solution {
  public:
    // Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int> arr, int n, long long k) {
        // Your code here
        long long sum=0;
        // arr[-1]=0;
        int slow=0,fast=0;
        while (sum!=k && fast!=n-1)
        {
            /* code */
            if(sum<k){
                sum+=arr[fast];
                fast++;
            }
            else if(sum>k){
                sum-=arr[slow];
                slow++;
            }
        }
        
    }
};

